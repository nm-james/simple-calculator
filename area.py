import math

#Area Calculations

def triangleArea(b, h, nil):
    return (b * h) / 2

def trampeziumArea(a, b, h):
    return ((a + b) / 2 ) * h

def rectangleArea(l, w, nil):
    return l * w

def circleArea(r, nil, null):
    return math.pi * (r ** 2)

def parallelogramArea(b, h, nil):
    return b * h